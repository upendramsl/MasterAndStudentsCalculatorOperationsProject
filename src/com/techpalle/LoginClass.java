package com.techpalle;

public class LoginClass 
{

private String em;
private String pass;
public LoginClass(String em, String pass) {
	super();
	
	this.em = em;
	this.pass = pass;
}
public String getEm() {
	return em;
}
public void setEm(String em) {
	this.em = em;
}
public String getPass() {
	return pass;
}
public void setPass(String pass) {
	this.pass = pass;
}



}
